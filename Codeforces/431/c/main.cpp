int n, k, d;
int memo[105][2];

int dfs(int s, bool hd) {
    int ans = 0;

    if (s == n && hd)
        return memo[s][hd] = 1;

    for (int i = 1; i <= k; i++) {
        if (s + i > n)
            return memo[s][hd] = ans;

        bool h = hd || (i >= d);

        if (memo[s+i][h] == -1)
            memo[s+i][h] = dfs(s + i, h);

        ans = plus_mod(ans, memo[s+i][h]);
    }

    return ans;
}

void solve() {
    read(n, k, d);

    memset(memo, -1, sizeof memo);

    cout << dfs(0, false);
}
